package com.sks.operators;

public class NumberComparison {
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 10;

        if (number1 > number2) {
            System.out.println("Larger Number is " + number1);
        } else {
            System.out.println("Smaller Number is " + number2);
        }
    }
}
